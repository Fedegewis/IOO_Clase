package uade.edu;

public class CerverzaDTO {

    private int codigoCerveza;

    private String nombreVaridad;

    private String nombrePresentacion;

    public int getCodigoCerveza() {
        return codigoCerveza;
    }

    public void setCodigoCerveza(int codigoCerveza) {
        this.codigoCerveza = codigoCerveza;
    }

    public String getNombreVaridad() {
        return nombreVaridad;
    }

    public void setNombreVaridad(String nombreVaridad) {
        this.nombreVaridad = nombreVaridad;
    }

    public String getNombrePresentacion() {
        return nombrePresentacion;
    }

    public void setNombrePresentacion(String nombrePresentacion) {
        this.nombrePresentacion = nombrePresentacion;
    }
}
